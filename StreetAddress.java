public class StreetAddress {

	private String house; //private variables
	private String city;
	private String state;
	private String zip;
	private String country;
	
	void setHouse(String h) //setting the type
	{
		house = h;
	}

	void setCity(String c) //setting the type
	{
		city = c;
	}

	void setState(String s) //setting the type
	{
		state = s;
	}

	void setZip(String z) //setting the type
	{
		zip = z;
	}

	void setCountry(String co) //setting the type
	{
		country = co;
	}

	public String toString () //toString() method passing variables to main
	{
		return house + "\n" + city + " " + state + " " + zip + " " + "\n" + country + "\n"; 
	}
}
